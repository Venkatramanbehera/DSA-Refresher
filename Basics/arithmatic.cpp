#include <iostream>
using namespace std;

// arithmatic operstors are + , - , /, *, %

int main()
{
    int a = 20, b = 14;
    cout << " a + b " << a + b << endl;
    cout << " a - b " << a - b << endl;
    cout << " a / b " << a / b << endl;
    cout << " a * b " << a * b << endl;
    cout << " a % b " << a % b << endl;
    return 0;
}